// Generate src/supabase/types/database.types.ts from the live database's
// PostgREST OpenAPI schema. Works with only SUPABASE_URL +
// SUPABASE_SERVICE_ROLE_KEY from .env — no Docker, DB password, or
// `supabase login` needed (unlike `supabase gen types --local`).
//
//   node scripts/gen-db-types.mjs
//
// Notes on fidelity vs. the official generator:
// - Nullability comes from the OpenAPI `required` list (NOT NULL columns).
// - Insert marks a column optional when it is nullable or has a default.
// - Relationship names are synthesized as `<table>_<column>_fkey`, which is
//   Postgres's default constraint naming (all our migrations rely on it).
// - RPC return types are not described by the spec, so Functions return Json.
import { readFileSync, mkdirSync, writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const root = join(dirname(fileURLToPath(import.meta.url)), '..');

// minimal .env parser (values never contain quotes/newlines in this project)
const env = {};
for (const line of readFileSync(join(root, '.env'), 'utf8').split(/\r?\n/)) {
  const m = line.match(/^([A-Z0-9_]+)=(.*)$/);
  if (m) env[m[1]] = m[2].trim();
}
const url = env.SUPABASE_URL;
const key = env.SUPABASE_SERVICE_ROLE_KEY;
if (!url || !key) {
  console.error('SUPABASE_URL / SUPABASE_SERVICE_ROLE_KEY missing from .env');
  process.exit(1);
}

const res = await fetch(`${url}/rest/v1/?apikey=${key}`, {
  headers: { Authorization: `Bearer ${key}` },
});
if (!res.ok) {
  console.error(`PostgREST schema fetch failed: ${res.status}`);
  process.exit(1);
}
const spec = await res.json();

function tsType(schema) {
  if (!schema) return 'unknown';
  const fmt = schema.format ?? '';
  if (schema.type === 'array')
    return `${tsType(schema.items)}[]`;
  if (fmt.startsWith('json') || schema.type === 'object') return 'Json';
  switch (schema.type) {
    case 'integer':
    case 'number':
      return 'number';
    case 'boolean':
      return 'boolean';
    case 'string':
    default:
      return 'string';
  }
}

const indent = (n) => '  '.repeat(n);

function tableBlock(name, def) {
  const cols = Object.keys(def.properties).sort();
  const notNull = new Set(def.required ?? []);
  const rel = [];
  const line = (col, optional, forceNullable) => {
    const p = def.properties[col];
    const nullable = !notNull.has(col) || forceNullable;
    return `${indent(4)}${col}${optional ? '?' : ''}: ${tsType(p)}${nullable ? ' | null' : ''};`;
  };

  for (const col of cols) {
    const m = (def.properties[col].description ?? '').match(
      /<fk table='([^']+)' column='([^']+)'\/>/,
    );
    if (m) {
      rel.push(
        `${indent(4)}{\n` +
          `${indent(5)}foreignKeyName: '${name}_${col}_fkey';\n` +
          `${indent(5)}columns: ['${col}'];\n` +
          `${indent(5)}isOneToOne: false;\n` +
          `${indent(5)}referencedRelation: '${m[1]}';\n` +
          `${indent(5)}referencedColumns: ['${m[2]}'];\n` +
          `${indent(4)}},`,
      );
    }
  }

  const row = cols.map((c) => line(c, false, false)).join('\n');
  const insert = cols
    .map((c) =>
      line(c, !notNull.has(c) || 'default' in def.properties[c], false),
    )
    .join('\n');
  const update = cols.map((c) => line(c, true, false)).join('\n');

  return (
    `${indent(2)}${name}: {\n` +
    `${indent(3)}Row: {\n${row}\n${indent(3)}};\n` +
    `${indent(3)}Insert: {\n${insert}\n${indent(3)}};\n` +
    `${indent(3)}Update: {\n${update}\n${indent(3)}};\n` +
    `${indent(3)}Relationships: ${rel.length ? `[\n${rel.join('\n')}\n${indent(3)}]` : '[]'};\n` +
    `${indent(2)}};`
  );
}

function functionBlock(path) {
  const name = path.slice('/rpc/'.length);
  const schema =
    spec.paths[path].post?.parameters?.find((p) => p.name === 'args' || p.in === 'body')
      ?.schema ?? { properties: {}, required: [] };
  const req = new Set(schema.required ?? []);
  const args = Object.keys(schema.properties ?? {})
    .sort()
    .map(
      (a) =>
        `${indent(4)}${a}${req.has(a) ? '' : '?'}: ${tsType(schema.properties[a])};`,
    )
    .join('\n');
  return (
    `${indent(2)}${name}: {\n` +
    `${indent(3)}Args: ${args ? `{\n${args}\n${indent(3)}}` : 'Record<string, never>'};\n` +
    `${indent(3)}Returns: Json;\n` +
    `${indent(2)}};`
  );
}

const tables = Object.keys(spec.definitions ?? {})
  .sort()
  .map((t) => tableBlock(t, spec.definitions[t]))
  .join('\n');
const fns = Object.keys(spec.paths)
  .filter((p) => p.startsWith('/rpc/'))
  .sort()
  .map(functionBlock)
  .join('\n');

const out = `// AUTO-GENERATED by scripts/gen-db-types.mjs — do not edit by hand.
// Regenerate with: npm run db:types
export type Json =
  | string
  | number
  | boolean
  | null
  | { [key: string]: Json | undefined }
  | Json[];

export type Database = {
  public: {
    Tables: {
${tables}
    };
    Views: Record<string, never>;
    Functions: {
${fns}
    };
    Enums: Record<string, never>;
    CompositeTypes: Record<string, never>;
  };
};

export type Tables<T extends keyof Database['public']['Tables']> =
  Database['public']['Tables'][T]['Row'];
export type TablesInsert<T extends keyof Database['public']['Tables']> =
  Database['public']['Tables'][T]['Insert'];
export type TablesUpdate<T extends keyof Database['public']['Tables']> =
  Database['public']['Tables'][T]['Update'];
`;

const outPath = join(root, 'src', 'supabase', 'types', 'database.types.ts');
mkdirSync(dirname(outPath), { recursive: true });
writeFileSync(outPath, out);
console.log(
  `Wrote ${outPath}: ${Object.keys(spec.definitions).length} tables, ` +
    `${Object.keys(spec.paths).filter((p) => p.startsWith('/rpc/')).length} functions`,
);
